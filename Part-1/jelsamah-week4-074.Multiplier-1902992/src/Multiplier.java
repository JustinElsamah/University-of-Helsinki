public class Multiplier {
    
    private int multiplier;
    
    public Multiplier(int multiplier){
        this.multiplier = multiplier;
    }
    
    public int multiply(int multiplicit){
        
        return (this.multiplier * multiplicit);
        
    }
    
    
}
